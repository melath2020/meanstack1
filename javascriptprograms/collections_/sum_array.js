let arr=[3,4,6]
//[10,9,7]
//method for adding an element push()
//method for removing an element pop()
var total=0;
for(let num of arr){
    total+=num;

}
console.log(total)//13

//13-4=9
//13-5=76
var res=[]
for(let num of arr){
    let item=total-num;
    res.push[item]

}
console.log(res)